(ns

    io.randomseed.utils.auth.specs

  (:require
   [clojure.spec.alpha       :as        s]
   [io.randomseed.utils      :as    utils]))

(s/def :io.randomseed.utils.auth/encrypt-fn ifn?)
(s/def :io.randomseed.utils.auth/check-fn   ifn?)
(s/def :io.randomseed.utils.auth/wait-fn    ifn?)

;; Single password (component of password entry),
;; plain and encrypted version

(s/def :io.randomseed.utils.auth.plain/password
 (s/or :string string? :bytes bytes?))

(s/def :io.randomseed.utils.auth.encrypted/password
  bytes?)

;; Handler identifier (part of a password entry)

(s/def :io.randomseed.utils.auth.password/handler-id
  qualified-symbol?)

;; Shared part of a password entry (after splitting)

(s/def :io.randomseed.utils.auth.password/shared
  (s/keys :req-un [:io.randomseed.utils.auth.password/handler-id]))

;; Intrinsic part of a password entry (after splitting)

(s/def :io.randomseed.utils.auth.password/intrinsic
  map?)

;; Dual password entry (shared and intrinsic parts as a map)

(s/def :io.randomseed.utils.auth.password/dual
  (s/keys :req-un [:io.randomseed.utils.auth.password/shared
                   :io.randomseed.utils.auth.password/intrinsic]))

;; Password entry

(s/def :io.randomseed.utils.auth/password
  (s/keys :req-un [:io.randomseed.utils.auth.password/handler-id]))

;; Password chain (sequence of password entries)

(s/def :io.randomseed.utils.auth.password-chain/last
  (s/keys :req-un [:io.randomseed.utils.auth.password/handler-id
                   :io.randomseed.utils.auth.encrypted/password]))

(s/def :io.randomseed.utils.auth/password-chain
  (s/and (s/coll-of :io.randomseed.utils.auth/password)
         #(s/valid? :io.randomseed.utils.auth.password-chain/last (last %))))

(s/def :io.randomseed.utils.auth.password-chain/shared
  (s/coll-of :io.randomseed.utils.auth.password/shared))

(s/def :io.randomseed.utils.auth.password-chain/intrinsic
  (s/and (s/coll-of :io.randomseed.utils.auth.password/intrinsic)
         #(s/valid? :io.randomseed.utils.auth.password-chain/last (last %))))

(s/def :io.randomseed.utils.auth.password-chain/dual
  (s/keys :req-un [:io.randomseed.utils.auth.password-chain/shared
                   :io.randomseed.utils.auth.password-chain/intrinsic]))

;; Settings are generated by parsing a configuration

(s/def :io.randomseed.utils.auth.settings.cipher.handler/id
  qualified-symbol?)

(s/def :io.randomseed.utils.auth.settings.cipher/handler-id
  :io.randomseed.utils.auth.settings.cipher.handler/id)

;; Cipher is a map of parameters used to create a password entry
;; during the encryption process. Multiple ciphers create a chain.

(s/def :io.randomseed.utils.auth.settings.cipher/name
  (s/or :string string? :identifier ident?))

(s/def :io.randomseed.utils.auth.settings.cipher/handler
  (s/keys :req-un [:io.randomseed.utils.auth/check-fn
                   :io.randomseed.utils.auth/encrypt-fn
                   :io.randomseed.utils.auth.settings.cipher.handler/id]))

(s/def :io.randomseed.utils.auth.settings/cipher
  (s/keys :req-un [:io.randomseed.utils.auth/check-fn
                   :io.randomseed.utils.auth/encrypt-fn
                   :io.randomseed.utils.auth.settings.cipher/handler
                   :io.randomseed.utils.auth.settings.cipher/name]
          :opt-un [:io.randomseed.utils.auth.settings.cipher/salt-charset
                   :io.randomseed.utils.auth.settings.cipher/salt-length
                   :io.randomseed.utils.auth.settings.cipher/salt-prefix
                   :io.randomseed.utils.auth.settings.cipher/salt-suffix]))

(s/def :io.randomseed.utils.auth.settings.cipher/shared
  (s/keys :req-un [:io.randomseed.utils.auth.settings.cipher/handler-id]))

(s/def :io.randomseed.utils.auth.settings.cipher/intrinsic
  map?)

(s/def :io.randomseed.utils.auth.settings.cipher/dual
  (s/keys :req-un [:io.randomseed.utils.auth.settings.cipher/shared
                   :io.randomseed.utils.auth.settings.cipher/intrinsic]))

(s/def :io.randomseed.utils.auth.settings/suite
  (s/coll-of :io.randomseed.utils.auth.settings/cipher))

(s/def :io.randomseed.utils.auth.settings.suite/shared
  (s/coll-of :io.randomseed.utils.auth.settings.cipher/shared))

(s/def :io.randomseed.utils.auth.settings.suite/intrinsic
  (s/coll-of :io.randomseed.utils.auth.settings.cipher/intrinsic))

(s/def :io.randomseed.utils.auth.settings.suite/dual
  (s/keys :req-un [:io.randomseed.utils.auth.settings.suite/shared
                   :io.randomseed.utils.auth.settings.suite/intrinsic]))

(s/def :io.randomseed.utils.auth.settings.cipher/salt-charset (s/nilable vector?))
(s/def :io.randomseed.utils.auth.settings.cipher/salt-prefix  (s/nilable string?))
(s/def :io.randomseed.utils.auth.settings.cipher/salt-suffix  (s/nilable string?))
(s/def :io.randomseed.utils.auth.settings.cipher/salt-length  (s/nilable nat-int?))

(s/def :io.randomseed.utils.auth.pwd/settings
  (s/keys :req-un [:io.randomseed.utils.auth.settings/suite]
          :opt-un [:io.randomseed.utils.auth/wait-fn]))

(s/def :io.randomseed.utils.auth.settings/generic
  (s/keys :opt-un [:io.randomseed.utils.auth/wait-fn]))

;; Authentication Configuration
;;
;; Data from configuration file which describes authentication parameters.
;; It also contains the :suite key which is a place for configuration
;; of ciphers. The ciphers are then used to process plain passwords
;; and produce encrypted versions represented as password chains.

(s/def :io.randomseed.utils.auth.config/wait        (s/nilable (s/and number? (s/or :pos pos? :zero zero?))))
(s/def :io.randomseed.utils.auth.config/wait-min    (s/nilable (s/and number? (s/or :pos pos? :zero zero?))))
(s/def :io.randomseed.utils.auth.config/wait-max    (s/nilable (s/and number? pos?)))
(s/def :io.randomseed.utils.auth.config/wait-nouser (s/nilable (s/or :pos pos? :zero zero?)))

(s/def :io.randomseed.utils.auth.config.cipher/handler      qualified-symbol?)
(s/def :io.randomseed.utils.auth.config.cipher/name         (s/or :string string? :identifier ident?))
(s/def :io.randomseed.utils.auth.config.cipher/salt-charset (s/nilable string?))
(s/def :io.randomseed.utils.auth.config.cipher/salt-prefix  (s/nilable string?))
(s/def :io.randomseed.utils.auth.config.cipher/salt-suffix  (s/nilable string?))
(s/def :io.randomseed.utils.auth.config.cipher/salt-length  (s/nilable nat-int?))

(s/def :io.randomseed.utils.auth.config/cipher
  (s/keys :req-un [:io.randomseed.utils.auth.config.cipher/handler
                   :io.randomseed.utils.auth.config.cipher/name]
          :opt-un [:io.randomseed.utils.auth.config.cipher/salt-charset
                   :io.randomseed.utils.auth.config.cipher/salt-length
                   :io.randomseed.utils.auth.config.cipher/salt-prefix
                   :io.randomseed.utils.auth.config.cipher/salt-suffix]))

(s/def :io.randomseed.utils.auth.config/suite
  (s/coll-of :io.randomseed.utils.auth.config/cipher :kind vector?))

(s/def :io.randomseed.utils.auth/config
  (s/keys :req-un [:io.randomseed.utils.auth.config/suite]
          :opt-un [:io.randomseed.utils.auth.config/wait
                   :io.randomseed.utils.auth.config/wait-min
                   :io.randomseed.utils.auth.config/wait-max
                   :io.randomseed.utils.auth.config/wait-nouser]))

(s/def :io.randomseed.utils.auth/crypto-entry
  (s/or :password        :io.randomseed.utils.auth/password
        :settings-cipher :io.randomseed.utils.auth.settings/cipher
        :config-cipher   :io.randomseed.utils.auth.config/cipher))

(s/def :io.randomseed.utils.auth/crypto-suite
  (s/or :password-suite :io.randomseed.utils.auth/password-chain
        :settings-suite :io.randomseed.utils.auth.settings/suite
        :config-suite   :io.randomseed.utils.auth.config/suite))
